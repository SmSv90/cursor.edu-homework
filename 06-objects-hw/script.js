function getSubjects(students) {
  let i = prompt('Choose number of students from 1 to 3');

  let arraySubjects = Object.keys(students[i-1].subjects).map( subject => {
    return (subject.charAt(0).toUpperCase() + subject.slice(1)).replace('_', ' ');
  });

  
  
  return arraySubjects.join(', ');

}

